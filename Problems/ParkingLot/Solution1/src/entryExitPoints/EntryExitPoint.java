package entryExitPoints;

public class EntryExitPoint {
    private double xCor;
    private double yCor;

    public EntryExitPoint(double xCor, double yCor) {
        this.xCor = xCor;
        this.yCor = yCor;
    }

    public double getxCor() {
        return xCor;
    }

    public void setxCor(double xCor) {
        this.xCor = xCor;
    }

    public double getyCor() {
        return yCor;
    }

    public void setyCor(double yCor) {
        this.yCor = yCor;
    }
}
